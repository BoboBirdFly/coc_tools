/**
 * 根据职业 ID 获取完整职业信息
 */
export const getFullProfessionById = (id: string): FullProfession | undefined => {
  return FULL_PROFESSIONS.find(p => p.id === id)
}

/**
 * 根据时代筛选职业
 */
export const getProfessionsByEra = (era: 'classic' | 'modern' | 'any'): FullProfession[] => {
  if (era === 'any') {
    return FULL_PROFESSIONS
  }
  return FULL_PROFESSIONS.filter(p => p.era === era || p.era === 'any')
}

